    @Test
    @Transactional
    void putNewStudent() throws Exception {
        // Initialize the database
        studentRepository.saveAndFlush(student);

        int databaseSizeBeforeUpdate = studentRepository.findAll().size();

        // Update the student
        Student updatedStudent = studentRepository.findById(student.getId()).get();
        // Disconnect from session so that the updates on updatedStudent are not directly saved in db
        em.detach(updatedStudent);
        updatedStudent
            .firstName(UPDATED_FIRST_NAME)
            .middleName(UPDATED_MIDDLE_NAME)
            .lastName(UPDATED_LAST_NAME)
            .gender(UPDATED_GENDER)
            .lrn(UPDATED_LRN)
            .yearLevel(UPDATED_YEAR_LEVEL)
            .strand(UPDATED_STRAND)
            .section(UPDATED_SECTION)
            .institutionalEmail(UPDATED_INSTITUTIONAL_EMAIL);

        restStudentMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedStudent.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedStudent))
            )
            .andExpect(status().isOk());

        // Validate the Student in the database
        List<Student> studentList = studentRepository.findAll();
        assertThat(studentList).hasSize(databaseSizeBeforeUpdate);
        Student testStudent = studentList.get(studentList.size() - 1);
        assertThat(testStudent.getFirstName()).isEqualTo(UPDATED_FIRST_NAME);
        assertThat(testStudent.getMiddleName()).isEqualTo(UPDATED_MIDDLE_NAME);
        assertThat(testStudent.getLastName()).isEqualTo(UPDATED_LAST_NAME);
        assertThat(testStudent.getGender()).isEqualTo(UPDATED_GENDER);
        assertThat(testStudent.getLrn()).isEqualTo(UPDATED_LRN);
        assertThat(testStudent.getYearLevel()).isEqualTo(UPDATED_YEAR_LEVEL);
        assertThat(testStudent.getStrand()).isEqualTo(UPDATED_STRAND);
        assertThat(testStudent.getSection()).isEqualTo(UPDATED_SECTION);
        assertThat(testStudent.getInstitutionalEmail()).isEqualTo(UPDATED_INSTITUTIONAL_EMAIL);
    }

